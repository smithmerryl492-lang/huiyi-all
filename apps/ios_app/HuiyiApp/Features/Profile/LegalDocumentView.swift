struct LegalSection: Identifiable, Sendable {
    var id: String { title }
    let title: String
    let body: String

    init(_ title: String, _ body: String) {
        self.title = title
        self.body = body
    }
}

struct LegalDocumentView: View {
    let document: LegalDocumentKind
    @EnvironmentObject private var router: AppRouter

    var body: some View {
        NavigationStack {
            List {
                Section {
                    Text(document.updatedAt)
                        .font(.footnote)
                        .foregroundStyle(HuiyiTheme.textSecondary)
                }

                ForEach(document.sections) { section in
                    Section(section.title) {
                        Text(section.body)
                            .font(.subheadline)
                            .lineSpacing(4)
                            .foregroundStyle(HuiyiTheme.textPrimary)
                    }
                }
            }
            .navigationTitle(document.title)
            .toolbar {
                Button("返回") { router.go(.profile) }
            }
        }
    }
}
